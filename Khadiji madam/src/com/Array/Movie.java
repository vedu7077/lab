package com.Array;

public class Movie {
	int mId;
	String mName;
	double mRating;
	
	public Movie() {

	
	}
	public int getmId() {
		return mId;
	}
	public void setmId(int mId) {
		this.mId = mId;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public double getmRating() {
		return mRating;
	}
	public void setmRating(double mRating) {
		this.mRating = mRating;
	}
	

}